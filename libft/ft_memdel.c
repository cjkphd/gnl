#include "libft.h"

void	ft_memdel(void **ap)
{
	if (ap && *ap)
	{
		free(*ap);
		*ap = NULL;
	}
}

/*
** Takes a parameter to the address of a memory area
** that needs to be freed with free(3)
** then puts the pointer to NULL.
*/
