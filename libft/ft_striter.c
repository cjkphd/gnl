#include "libft.h"

void	ft_striter(char *s, void (*f)(char *))
{
	int	i;

	i = 0;
	if (s && f)
	{
		while (s[i])
		{
			f(&s[i]);
			i++;
		}
	}
}

/*
** The *f is applied to each (char *) of the string being passed as an arg.
** Each character passed by address to F, which can be modified when necessary.
*/
